void main() {
  // Question: How do you convert a string to uppercase in Dart?
  // Answer: You can convert a string to uppercase in Dart using the `toUpperCase()` method.

  String myString = "hello";
  String uppercaseString = myString.toUpperCase(); // "HELLO"
  print("toUpperCase: $uppercaseString");
}
